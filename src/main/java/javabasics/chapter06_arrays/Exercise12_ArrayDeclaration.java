package main.java.javabasics.chapter06_arrays;

/**
 * <h3>🎯 문제명: 여러 개의 상자를 한번에 만들기 - 배열!</h3>
 * 
 * <h3>📋 문제 설명</h3>
 * <p>
 * 지금까지는 변수를 하나씩 만들었죠? 🎁<br>
 * 만약 학생 30명의 점수를 저장하려면... 변수를 30개나 만들어야 할까요? 😱<br><br>
 * 
 * 걱정 마세요! <b>배열(Array)</b>이 있어요! 🎉<br>
 * 배열은 <b>같은 종류의 상자를 여러 개 붙여놓은 것</b>이에요.<br>
 * 마치 아파트처럼 101호, 102호, 103호... 이렇게 번호가 있어요!
 * </p>
 * 
 * <h3>📌 학습 목표</h3>
 * 
 *   <ul>
 *     <li>✅ 배열이 무엇인지 이해하기</li>
 *     <li>✅ 배열을 만드는 방법 배우기</li>
 *     <li>✅ 배열의 각 칸에 값 넣기</li>
 *   </ul>
 * 
 * 
 * <h3>📝 배열의 기초 개념</h3>
 * <table border="1">
 *   <tr>
 *     <th>개념</th>
 *     <th>설명</th>
 *     <th>예시</th>
 *   </tr>
 *   <tr>
 *     <td>배열 선언</td>
 *     <td>타입[] 이름 = new 타입[크기]</td>
 *     <td><code>int[] scores = new int[5];</code></td>
 *   </tr>
 *   <tr>
 *     <td>인덱스</td>
 *     <td>배열의 각 칸 번호 (0부터 시작!)</td>
 *     <td>0번, 1번, 2번, 3번, 4번</td>
 *   </tr>
 *   <tr>
 *     <td>값 넣기</td>
 *     <td>배열이름[인덱스] = 값</td>
 *     <td><code>scores[0] = 100;</code></td>
 *   </tr>
 * </table>
 * 
 * <h3>🏢 배열을 아파트로 생각해보세요!</h3>
 * 
 *   <pre>
 *   배열: numbers (5개짜리)
 *   
 *   ┌────┬────┬────┬────┬────┐
 *   │ 0호│ 1호│ 2호│ 3호│ 4호│  ← 인덱스 (호수)
 *   ├────┼────┼────┼────┼────┤
 *   │    │    │    │    │    │  ← 값 (입주자)
 *   └────┴────┴────┴────┴────┘
 *   
 *   numbers[0] = 10;  // 0호에 10 입주!
 *   numbers[2] = 30;  // 2호에 30 입주!
 *   </pre>
 * 
 * 
 * <h3>⚠️ 초보자가 자주하는 실수</h3>
 * 
 *   <table>
 *     <tr>
 *       <th>❌ 잘못된 예</th>
 *       <th>✅ 올바른 예</th>
 *       <th>💭 설명</th>
 *     </tr>
 *     <tr>
 *       <td><code>int numbers[] = new int[5];</code></td>
 *       <td><code>int[] numbers = new int[5];</code></td>
 *       <td>[]는 타입 뒤에!</td>
 *     </tr>
 *     <tr>
 *       <td><code>numbers[5] = 50;</code></td>
 *       <td><code>numbers[4] = 50;</code></td>
 *       <td>5개짜리는 0~4번까지!</td>
 *     </tr>
 *     <tr>
 *       <td><code>int[] numbers = new int[];</code></td>
 *       <td><code>int[] numbers = new int[5];</code></td>
 *       <td>크기를 꼭 정해주세요!</td>
 *     </tr>
 *   </table>
 * 
 * 
 * <h3>🎮 도전 과제</h3>
 * <p>5개짜리 정수 배열을 만들고, 0번째와 2번째에 값을 넣어보세요!</p>
 */
public class Exercise12_ArrayDeclaration {
    public static void main(String[] args) {
        /**
         * 🧑‍💻 여기에 코드를 작성하세요!
         * 
         * 📝 1단계: 5개짜리 정수 배열 만들기
         * int[]  numbers  =  new int[5];
         * ─────  ───────     ──────────
         * 타입    이름        5개 준비!
         */
        
        int[] numbers = new int[5];  // 🏢 5개짜리 아파트 완성!
        
        /**
         * 📝 2단계: 배열에 값 넣기
         * numbers[0] = 10;  // 0호에 10 입주
         * numbers[2] = 30;  // 2호에 30 입주
         */
        
        numbers[0] = 10;  // 🏠 첫 번째 칸(0번)에 10 저장!
        numbers[2] = 30;  // 🏠 세 번째 칸(2번)에 30 저장!
        
        
        /**
         * 🖥️ 결과 확인하기
         * 아래 코드가 배열의 값을 출력해줄 거예요!
         */
        System.out.println("첫 번째 요소: " + numbers[0]);
        System.out.println("세 번째 요소: " + numbers[2]);
        
        /**
         * 📋 예상 결과
         * ┌─────────────────────────┐
         * │ 첫 번째 요소: 10        │
         * │ 세 번째 요소: 30        │
         * └─────────────────────────┘
         * 
         * 💡 배열의 현재 상태:
         * ┌────┬────┬────┬────┬────┐
         * │ 10 │  0 │ 30 │  0 │  0 │
         * └────┴────┴────┴────┴────┘
         *   0번  1번  2번  3번  4번
         * 
         * 🤔 왜 나머지는 0일까요?
         * - int 배열을 만들면 처음에 모든 칸이 0으로 채워져요!
         * 
         * 🚀 추가 연습:
         * - 모든 칸에 값을 넣어보세요
         * - 배열의 크기를 10으로 늘려보세요
         * - String 배열도 만들어보세요
         */
    }
}